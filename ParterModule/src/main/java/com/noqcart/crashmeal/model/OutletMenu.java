package com.noqcart.crashmeal.model;

public class OutletMenu {
	private Long outletMenuId;
	private Long menuId;
	private Long partnerId;
	public Long getOutletMenuId() {
		return outletMenuId;
	}
	public void setOutletMenuId(Long outletMenuId) {
		this.outletMenuId = outletMenuId;
	}
	public Long getMenuId() {
		return menuId;
	}
	public void setMenuId(Long menuId) {
		this.menuId = menuId;
	}
	public Long getPartnerId() {
		return partnerId;
	}
	public void setPartnerId(Long partnerId) {
		this.partnerId = partnerId;
	}
	
}
